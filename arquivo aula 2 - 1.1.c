#include <stdio.h>

/*
Criem um programa que recebe o número X do usuário
e se o número for maior que 5 imprimir → "X é maior que 5."
*/

void main (){
     int y = 5;
     int x;
     printf("digite um valor: ");
     scanf("%d",&x);

     if (x > y){
      printf("x é maior que 5");
      }
}
